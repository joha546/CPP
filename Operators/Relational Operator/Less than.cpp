/*
This operator is used to check whether the first operand is less than the second operand or not. It returns true(1) 
if the first operand is less than the second operand else returns false(0).
*/

#include<iostream>
using namespace std;

int main() {
    int a = 10;
    int b = 15;
    cout << (a < b);
    // It returns true because the first operand is smaller than the second
    return 0;
}