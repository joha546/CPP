/*
This operator is used to check whether the first operand is greater than or equal to the second operand or not. 
It returns true(1) if the first operand is greater than or equal to the second operand; otherwise, it returns 
false(0).
*/

#include<iostream>
using namespace std;

int main() {
    int a = 10;
    int b = 8;
    cout << (a >= b);
    // It returns true because the first operand is greater than the second
    return 0;
}
